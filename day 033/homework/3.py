# color - ის დახმარებით ჩვენ შეგვიძლია შევუსვალოთ ფერი ნებისმიერ ელემენტს გარდა ფოტორბისა.
# background-color - ის დახმარებით ჩვენ შეგვიძლია რომ საიტის backgroun-ი ასევე ტეხსტის background.
# font-size - ის დახმარებით ჩვენ შეგვიძლიათ რომ ზომა შევუცვალოთ ტექსტს.
# text-align - ის დახმარებით ჩვენ შეგვიძლია რამდენიმე ელემენტი გარკვეულ ადგილას გადავიტანოთ.